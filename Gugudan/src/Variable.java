
public class Variable {
	public static void main(String[] args)
	{
		//논리형
				boolean booleanType = true;
				
				//문자형
				char charType = 'a';
				
				//정수형 int, long, byte(이진데이터를 위한), short
				byte byteType = 100;
				short shortType = 32767;
				int intType = 214544232;
				long longType = 92151252151255L;
				
				//실수형 float, double
				float floatType = 3.1252152151F;
				double doubleType = 1.723464646462;
				
				System.out.println(booleanType);
				System.out.println(charType);
				System.out.println(byteType);
				System.out.println(shortType);
				System.out.println(intType);
				System.out.println(longType);
				System.out.println(floatType);
				System.out.println(doubleType);
	}

}
